    public List<Map<String, Integer>> getLevelTwoCondition() {
        return Arrays.stream(getLevenOneNoAndLevelTwoNoArr()).map(x -> {
            String[] arr = StringUtils.split(x, ".");
            Map<String, Integer> map = new HashMap<>();
            map.put(PARAM_LEVEL_ONE_NO, Integer.parseInt(arr[0]));
            map.put(PARAM_LEVEL_TWO_NO, Integer.parseInt(arr[1]));
            return map;
        }).collect(Collectors.toList());
    }

